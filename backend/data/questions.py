def get_questions_for_age(age_months: int) -> list[dict[str, str]]:
    for min_m, max_m in sorted(QUESTIONS.keys(), reverse=True):
        if min_m <= age_months <= max_m:
            return QUESTIONS[(min_m, max_m)]
    for min_m, max_m in sorted(QUESTIONS.keys()):
        if age_months <= max_m:
            return QUESTIONS[(min_m, max_m)]
    return QUESTIONS[(10, 12)]
